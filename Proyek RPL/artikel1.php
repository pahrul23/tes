<p class="narasi-description">&emsp;Penyerahan bantuan kepada warga yang membutuhkan dan insentif Desa Taraweang, Kecamatan Labakkang, Kabupaten Pangkep, diberikan langsung oleh Bupati Pangkep, Muhammad Yusran Lalogau.
Pemberian bantuan kepada segenap warga ini berlangsung di balai pertemuan Baruga Sayang Desa yang padat dan membludak tersebut pada hari Sabtu, 24 April 2021.
Bantuan yang diserahkan meliputi BLT, bak air, insentif perangkat/staf, kepala dusun, DPD, kader posyandu, KPM, RK/RT, guru mengaji, guru TPA, pemandi jenazah dan penggali kubur, imam masjid dan pembantu imam masjid.
<br><br>&emsp;Kegiatan dipimpin oleh Kepala Desa Taraweang, Amiruddin yang didampingi Ketua Tim penggerak PKK diikut segenap staf dan perangkat desa ini dihadiri pula Rizki Darmawansyah (Anggota DPRD Pangkep), H. Abd. Haris (Camat Labakkang), Jonathan P. (Danramil Labakkang), Babinsa Karyo (Babinsa), Muh. Arifin (Babinkamtibmas), dan Mardini Burhan TA PP P3MD Pangkep.
Dalam sambutan Kades Amiruddin menyampaikan, Alhamdulillah ini satu kesukuran karena yang hadir langsung menyerahkan bantuan dan insentif ini adalah Bupati Pangkajene dan Kepulauan di mana begitu sibuknya beliau menyempatkan diri.
<br><br>&emsp;Dikatakannya, dari awal saya jadi kepala desa bantuan bak air sebelum saya masuk itu masih ada 700 kepala keluarga atau rumah yang belum mendapatkan bantuan bak air, setelah saya menjadi kepala desa masuk tahun ketiga saya berikan ke masyarakat 383 unit bak air. Berarti masih ada 280 lebih yang menjadi tanggung jawab kita bersama yang menjadi tumpuan harapan masyarakat kita mudah-mudahan kedepannya bisa dituntaskan dalam waktu 2 tahun ke depan.
Ada berbagai macam bantuan bantuan yang kita gelontorkan kepada masyarakat. Tahun ini kita mengadakan mobil sampah atau mobil Viar untuk mengangkut sampah sampah masyarakat sehingga ini merupakan program Pak Bupati program pemerintah daerah bagaimana Pangkep lebih bersih lebih indah setelah Pak Bupati menjadi Bupati Pangkep sekarang, ungkapnya
Sementara oleh Bupati Pangkep Yusran, disampaikan bahwa penyerahan bantuan ini, selain BLT, ini ada bantuan bak air dainya minta dan berharap warga Untuk tidak saling cemburu, kita nanti siapkan juga fasilitas bak sampah besar di tiap desa untuk menunjang sarana kebersihan hingga ke pelosok desa.



