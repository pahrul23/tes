nasa dan perusahaan-perusahaan tambang yang ada di Pangkep, pesan Bissu Saidi penting untuk dijadikan acuan. Sehingga pengelolaan sumber daya alam yang ada di Pangkep tidak hanya mengambil apa yang telah disediakan alam untuk manusia, tetapi menjaganya supaya dapat terus bermanfaat dan tidak justru menjadi bencana bagi mahluk hidup khususnya yang ada disekitar lokasi tambang.</p>      
                
